// Fonction pour calculer la population projetée
function calculerPopulationProjetee(populationInitiale, tauxAccroissement, anneeInitiale, anneeHorizon) {
    return populationInitiale * Math.pow(1 + tauxAccroissement, anneeHorizon - anneeInitiale);
}

// Fonction pour déterminer la catégorie de capacité
function determinerCategorieCapacite(capacite) {
    if (capacite < 20000) {
        return '<20000';
    } else if (capacite <= 50000) {
        return '20000 - 50000';
    } else {
        return '>50000';
    }
}

// Fonction pour évaluer les alternatives avec la méthode AHP
function evaluerAlternativesAHP(siteConditions) {
    const scores = {};
    const priseTypes = Object.keys(ahpData.alternativeScores);
    
    // Calculer les scores pour chaque alternative
    for (const priseType of priseTypes) {
        let score = 0;
        for (const [criterion, weight] of Object.entries(ahpData.criteriaWeights)) {
            if (siteConditions[criterion]) {
                score += weight * ahpData.alternativeScores[priseType][siteConditions[criterion]];
            }
        }
        scores[priseType] = score;
    }
    
    // Trier les alternatives par score (du plus petit au plus grand pour l'AHP)
    return Object.entries(scores)
        .sort((a, b) => a[1] - b[1])
        .map(([name, score], index) => ({
            rang: index + 1,
            code: name,
            nom: ahpData.descriptions[name].title,
            score: score.toFixed(2),
            description: ahpData.descriptions[name].description
        }));
}